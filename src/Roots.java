public class Roots {
    private Double root1;
    private Double root2;

    public Roots(Double root1, Double root2) {
        this.root1 = root1;
        this.root2 = root2;
    }

    public Double getRoot1() {
        return root1;
    }

    public Double getRoot2() {
        return root2;
    }

    public void setRoot1(double root1) {
        this.root1 = root1;
    }

    public void setRoot2(double root2) {
        this.root2 = root2;
    }
}
